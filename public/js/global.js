function push_error(code, target, message) {

    var icon = 'fa fa-info';
    var wrapperclass = 'alert-info';
    var fadeOutspeed = 3500;
    var fadeinSpeed = 1800;

    if (code === 404) {
        message = 'Route wurde in Laravel noch nicht definiert - die Funktion ist ggf. nicht implementiert.';
    }

    if (code === 403) {
        message = 'Dein Account hat nicht die Rechte zum AusfÃ¼hren dieser Aktion.';
    }

    if (code === 422) {
        icon = 'fa fa-warning';
        wrapperclass = 'alert-danger';
        message = 'Der Validierer hat vermutlich festgestellt, dass eine Eingabe keinen korrekten Wert hat (z.B: invalide E-Mail) oder einfach fehlt (fehlende Angabe von Gruppen).';
    }

    if (code >= 500) {

        icon = 'fa fa-warning';
        wrapperclass = 'alert-danger';
        message = 'Es trat ein schwerer Serverfehler (Code 5xx) auf, am besten meldest du das dem Programmirer.';
    }


    $(target).html('<i class="' + icon + '"></i> ' + message).addClass(wrapperclass).fadeIn(fadeinSpeed, function () {
        $(this).delay(fadeOutspeed).fadeOut("slow");
    });
}


